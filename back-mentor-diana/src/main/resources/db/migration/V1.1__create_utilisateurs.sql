CREATE TABLE DIANA_MENTOR.UTILISATEUR (
   UTILISATEUR_ID               INT8                 not null,
   UTILISATEUR_NOM              VARCHAR(250)         null,
   UTILISATEUR_PRENOM           VARCHAR(250)         null,
   UTILISATEUR_PASSWORD           VARCHAR(100)         null,
   UTILISATEUR_INUTILISABLE_FLAG BOOL                 null,
   UTILISATEUR_EMAIL             VARCHAR(250)         not null,
   constraint PK_UTILISATEUR primary key (UTILISATEUR_ID)
);

